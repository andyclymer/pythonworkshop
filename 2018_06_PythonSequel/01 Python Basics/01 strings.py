from __future__ import print_function

# The most basic object is a string. Strings can be single- or double-quoted.
# Here, we are defining a variable for that string.
useful_greeting = 'Good morning, Advanced Python students!'
print(useful_greeting)
print()
print('A very', useful_greeting)
print()
print(useful_greeting * 10)

# This is a multiline string.
another_greeting = '''
So far, you are the best students we had today. Good morning.

We enjoy this class very much.
'''

print(another_greeting)
